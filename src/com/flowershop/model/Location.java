public class Location {
    private float lat;
    private float lng;

    public float getLat() {
        return lat;
    }

    public void setLat(float latitude) {
        this.lat = latitude;
    }

    public float getLng() {
        return lng;
    }

    public void setLng(float longitude) {
        this.lng = longitude;
    }
}
